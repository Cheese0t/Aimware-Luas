			end
			
		draw.Color(85,58,73,255)
		draw.FilledRect(windowx - 50 ,windowy + windowsizey + 44, windowx + 450 ,windowy + windowsizey + 48)

			if starttextpos > 0 then
				if starttextpos > 250 then
					draw.Color(0,0,0,75)
					draw.SetFont(font4)
